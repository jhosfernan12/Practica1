public class Persona2 
{
    private String nombre;
    private int edad;
    public Persona2(String nombre, int edad) 
    {
        this.nombre = nombre;
        this.edad = edad;
    }
        public void setNombre(String nombre) 
    {
        this.nombre = nombre;
    }

    public String getNombre() 
    {
        return this.nombre;
    }
    public void setEdad(int edad) 
    {
        this.edad = edad;
    }
    public int getEdad() 
    {
        return this.edad;
    }

}
